public class String_Concat {

	/**
	 * @param args
	 */
	public static void main(String[] args)
	{
		int a=10;
		int b=20;
		String x= "Hello";
		String y="World";
		double c= 12.33;
		double d= 10.33;
		
		System.out.println(a+b);
		System.out.println(x+y);
		System.out.println(c+d);
		System.out.println(a+b+(x+y)+c+d);
	}

}
